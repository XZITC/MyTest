public class AceSendBP {

	public SendVO[] queryByQueryScheme(IQueryScheme querySheme) {
		QuerySchemeProcessor p = new QuerySchemeProcessor(querySheme);
		p.appendFuncPermissionOrgSql();
		return new SchemeVOQuery<SendVO>(SendVO.class).query(querySheme,
				null);
	}
}
